"""Build instruction-following (IF) dataset from BCT knowledge prompts.

Each sample = a BCT knowledge prompt wrapped with a verifiable format instruction.
Responses are generated by GPT-4o-mini and filtered by a deterministic verifier.

Output format (one JSON per line):
    {
        "prompt": str,           # original BCT prompt (unwrapped)
        "wrapped_prompt": str,   # prompt + wrapper instruction
        "response": str,         # model response (passes verifier)
        "wrapper_id": str,       # e.g. "lowercase", "json", "word_limit_50"
        "wrapper_text": str,     # the instruction text that was appended
        "verified": bool,        # always True (failed samples are dropped)
    }

Usage:
    # Smoke test (10 samples, inspect manually):
    python data/scripts/build_if_dataset.py --smoke

    # Full run (500 samples):
    python data/scripts/build_if_dataset.py --n-samples 500 --out data/processed/if_dataset.jsonl

Cost estimate (GPT-4o-mini, 500 samples):
    ~$0.07 total (input: ~40k tok @ $0.15/1M, output: ~100k tok @ $0.60/1M)
"""

from __future__ import annotations

import argparse
import json
import random
import re
import sys
import urllib.request
from dataclasses import dataclass
from pathlib import Path
from typing import Callable

from dotenv import load_dotenv
from openai import OpenAI

load_dotenv()

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------

BCT_URL = "https://github.com/raybears/cot-transparency/raw/main/dataset_dumps/control_seed_42/instruct_samples.jsonl"
MODEL = "gpt-4o-mini"
DEFAULT_OUT = Path("data/processed/if_dataset.jsonl")
SMOKE_OUT = Path("data/processed/if_dataset_smoke.jsonl")
SMOKE_N = 10
DEFAULT_N = 500
SEED = 42

SYSTEM_PROMPT = (
    "You are a helpful assistant. Follow the user's instructions carefully and precisely. "
    "Do not add commentary about the instruction itself — just comply with it."
)

# ---------------------------------------------------------------------------
# Wrapper definitions
# ---------------------------------------------------------------------------

@dataclass
class Wrapper:
    id: str
    text: str                       # instruction appended to prompt
    verify: Callable[[str], bool]   # returns True if response complies


def _word_count(s: str) -> int:
    return len(s.split())


def _sentence_count(s: str) -> int:
    # Split on . ! ? followed by whitespace or end
    sentences = re.split(r"[.!?]+(?:\s+|$)", s.strip())
    return len([s for s in sentences if s.strip()])


def _paragraph_count(s: str) -> int:
    paras = [p for p in s.strip().split("\n\n") if p.strip()]
    return len(paras)


def _is_valid_json(s: str) -> bool:
    # Strip markdown code fences if present
    stripped = re.sub(r"^```(?:json)?\s*|```\s*$", "", s.strip(), flags=re.MULTILINE).strip()
    try:
        json.loads(stripped)
        return True
    except (json.JSONDecodeError, ValueError):
        return False


def _has_numbered_list(s: str) -> bool:
    return bool(re.search(r"^\s*\d+[\.\)]\s+\S", s, re.MULTILINE))


def _has_bullet_list(s: str) -> bool:
    return bool(re.search(r"^\s*[-*•]\s+\S", s, re.MULTILINE))


WRAPPERS: list[Wrapper] = [
    Wrapper(
        id="lowercase",
        text="Your entire response must be in lowercase letters only.",
        verify=lambda r: r == r.lower() and len(r.strip()) > 0,
    ),
    Wrapper(
        id="uppercase",
        text="Your entire response must be in UPPERCASE letters only.",
        verify=lambda r: r == r.upper() and len(r.strip()) > 0,
    ),
    Wrapper(
        id="word_limit_50",
        text="Answer in fewer than 50 words.",
        verify=lambda r: _word_count(r) < 50 and _word_count(r) > 0,
    ),
    Wrapper(
        id="word_min_150",
        text="Your response must be at least 150 words long.",
        verify=lambda r: _word_count(r) >= 150,
    ),
    Wrapper(
        id="one_sentence",
        text="Answer in exactly one sentence.",
        verify=lambda r: _sentence_count(r) == 1 and len(r.strip()) > 0,
    ),
    Wrapper(
        id="three_paragraphs",
        text="Structure your response in exactly 3 paragraphs, separated by blank lines.",
        verify=lambda r: _paragraph_count(r) == 3,
    ),
    Wrapper(
        id="json",
        text='Respond only with a valid JSON object. Do not include any text outside the JSON.',
        verify=_is_valid_json,
    ),
    Wrapper(
        id="numbered_list",
        text="Format your entire response as a numbered list (e.g. 1. ... 2. ... 3. ...).",
        verify=_has_numbered_list,
    ),
    Wrapper(
        id="bullet_list",
        text="Format your entire response as a bullet list using dashes (- item).",
        verify=_has_bullet_list,
    ),
    Wrapper(
        id="start_with_answer",
        text='Start your response with the word "Answer:" (exactly, including the colon).',
        verify=lambda r: r.lstrip().startswith("Answer:"),
    ),
    Wrapper(
        id="end_phrase",
        text='End your response with the exact phrase: "That is the complete explanation."',
        verify=lambda r: r.rstrip().endswith("That is the complete explanation."),
    ),
    Wrapper(
        id="no_word_however",
        text='Do not use the word "however" anywhere in your response.',
        verify=lambda r: "however" not in r.lower(),
    ),
    Wrapper(
        id="wrap_in_quotes",
        text="Wrap your entire response in double quotation marks.",
        verify=lambda r: r.strip().startswith('"') and r.strip().endswith('"') and len(r.strip()) > 2,
    ),
]

WRAPPER_BY_ID = {w.id: w for w in WRAPPERS}

# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------

def load_bct_prompts(n: int, seed: int = SEED) -> list[str]:
    """Stream BCT JSONL from GitHub, return n shuffled user prompts."""
    print(f"Fetching BCT prompts from {BCT_URL} ...")
    req = urllib.request.Request(BCT_URL, headers={"User-Agent": "Mozilla/5.0"})
    prompts = []
    with urllib.request.urlopen(req, timeout=60) as resp:
        for line in resp:
            line = line.decode("utf-8").strip()
            if not line:
                continue
            try:
                obj = json.loads(line)
                user_msg = obj["messages"][0]["content"]
                prompts.append(user_msg)
            except (json.JSONDecodeError, KeyError, IndexError):
                continue

    print(f"Loaded {len(prompts)} BCT prompts")
    rng = random.Random(seed)
    rng.shuffle(prompts)
    return prompts[:n]


# ---------------------------------------------------------------------------
# Generation
# ---------------------------------------------------------------------------

def build_wrapped_prompt(base_prompt: str, wrapper: Wrapper, as_prefix: bool) -> tuple[str, str]:
    """Return (wrapped_prompt, placement) where placement is 'prefix' or 'suffix'."""
    if as_prefix:
        return f"{wrapper.text}\n\n{base_prompt}", "prefix"
    else:
        return f"{base_prompt}\n\n{wrapper.text}", "suffix"


def generate_response(client: OpenAI, wrapped_prompt: str) -> str:
    resp = client.chat.completions.create(
        model=MODEL,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": wrapped_prompt},
        ],
        temperature=0.7,
        max_tokens=512,
    )
    return resp.choices[0].message.content or ""


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def run(n_samples: int, out_path: Path, seed: int = SEED, wrapper_ids: list[str] | None = None) -> None:
    client = OpenAI()  # reads OPENAI_API_KEY from env

    wrappers = [WRAPPER_BY_ID[wid] for wid in wrapper_ids] if wrapper_ids else WRAPPERS

    # We need more BCT prompts than n_samples because some generations will fail verification
    # and we sample wrappers randomly, so over-fetch by 2x
    bct_prompts = load_bct_prompts(n=n_samples * 2, seed=seed)

    rng = random.Random(seed)
    results = []
    attempts = 0
    verification_failures = 0

    for i, prompt in enumerate(bct_prompts):
        if len(results) >= n_samples:
            break

        wrapper = rng.choice(wrappers)
        as_prefix = rng.random() < 0.5
        wrapped, placement = build_wrapped_prompt(prompt, wrapper, as_prefix)

        attempts += 1
        try:
            response = generate_response(client, wrapped)
        except Exception as e:
            print(f"  [API error] {e}", file=sys.stderr)
            continue

        passed = wrapper.verify(response)
        if not passed:
            verification_failures += 1

        record = {
            "prompt": prompt,
            "wrapped_prompt": wrapped,
            "response": response,
            "wrapper_id": wrapper.id,
            "wrapper_text": wrapper.text,
            "wrapper_placement": placement,
            "verified": passed,
        }
        results.append(record)

        status = "OK" if passed else "FAIL"
        print(f"  [{len(results):3d}/{n_samples}] wrapper={wrapper.id:<20s} verify={status}  ({_word_count(response)} words)")

        # In smoke mode, print full response for manual inspection
        if n_samples <= SMOKE_N:
            print(f"    PROMPT:   {prompt[:80]}...")
            print(f"    WRAPPER:  {wrapper.text}")
            print(f"    RESPONSE: {response[:300]}")
            print()

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        for r in results:
            f.write(json.dumps(r) + "\n")

    verified = sum(1 for r in results if r["verified"])
    print(f"\nDone. {len(results)} samples written to {out_path}")
    print(f"  Verified: {verified}/{len(results)} ({100*verified/max(1,len(results)):.1f}%)")
    print(f"  Verification failures: {verification_failures}/{attempts} attempts")

    # Per-wrapper breakdown
    from collections import Counter
    wrapper_counts = Counter(r["wrapper_id"] for r in results)
    wrapper_pass = Counter(r["wrapper_id"] for r in results if r["verified"])
    print("\nPer-wrapper verification rate:")
    for wid, total in sorted(wrapper_counts.items()):
        passed = wrapper_pass[wid]
        print(f"  {wid:<25s}: {passed}/{total} ({100*passed/total:.0f}%)")


def main():
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--smoke", action="store_true", help=f"Run smoke test with {SMOKE_N} samples")
    parser.add_argument("--n-samples", type=int, default=DEFAULT_N, help="Number of samples to generate")
    parser.add_argument("--out", type=Path, default=None, help="Output JSONL path")
    parser.add_argument(
        "--wrappers",
        nargs="+",
        choices=sorted(WRAPPER_BY_ID.keys()),
        help="Subset of wrappers to use (default: all)",
    )
    parser.add_argument("--seed", type=int, default=SEED)
    args = parser.parse_args()

    if args.smoke:
        n = SMOKE_N
        out = args.out or SMOKE_OUT
    else:
        n = args.n_samples
        out = args.out or DEFAULT_OUT

    # Cost estimate
    avg_input_tok = 80   # system (~50) + wrapper (~15) + prompt (~16)
    avg_output_tok = 200
    input_cost = n * avg_input_tok * 0.15 / 1_000_000
    output_cost = n * avg_output_tok * 0.60 / 1_000_000
    print(f"Estimated cost ({MODEL}, {n} samples): ${input_cost + output_cost:.4f}")
    print(f"  Input:  ~{n * avg_input_tok:,} tokens  (${input_cost:.4f})")
    print(f"  Output: ~{n * avg_output_tok:,} tokens  (${output_cost:.4f})")
    print()

    run(n_samples=n, out_path=out, seed=args.seed, wrapper_ids=args.wrappers)


if __name__ == "__main__":
    main()
